package com.zhangyongcun.myclass;

public class Order {
	private String o_id;
	private String m_id;
	
	
	
	
	
	
	public Order() {
		super();
	}
	public Order(String o_id, String m_id) {
		super();
		this.o_id = o_id;
		this.m_id = m_id;
	}
	public String getO_id() {
		return o_id;
	}
	public void setO_id(String o_id) {
		this.o_id = o_id;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	
	
	
	
	
	
	
	
	
}
